export function getAreaData(slug: string): AreaData | null {
  return areaDataMap[slug.toLowerCase()] || null;
}

export function getAllAreaSlugs(): string[] {
  return Object.keys(areaDataMap);
}

export function getAreaPropertiesPaginated(
  slug: string, 
  page: number = 1, 
  itemsPerPage: number = 8
): { properties: Property[]; hasMore: boolean; totalItems: number } {
  const areaData = areaDataMap[slug.toLowerCase()];
  
  if (!areaData) {
    return { properties: [], hasMore: false, totalItems: 0 };
  }

  const startIndex = (page - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const properties = areaData.properties.slice(startIndex, endIndex);
  const hasMore = endIndex < areaData.properties.length;

  return {
    properties,
    hasMore,
    totalItems: areaData.properties.length
  };
}
