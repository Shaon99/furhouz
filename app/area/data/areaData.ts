// Helper function to generate slug from title
const generateSlug = (title: string): string => {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '') // Remove special characters
    .replace(/\s+/g, '-') // Replace spaces with hyphens
    .replace(/-+/g, '-') // Replace multiple hyphens with single hyphen
    .trim();
};

// Helper function to get location from road
const getLocationFromRoad = (road: string): string => {
  if (road.includes('Gulshan')) return 'Gulshan';
  if (road.includes('Banani')) return 'Banani';
  if (road.includes('Baridhara')) return 'Baridhara';
  if (road.includes('Bashundhara')) return 'Bashundhara';
  return 'Dhaka';
};

